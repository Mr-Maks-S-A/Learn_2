#pragma once
#include <sstream>
#include <cmath>
#include <Until/Input.hpp>


/**
 * @class Calculator
 * @brief Шаблонный класс для синтаксического анализа (парсинга) и вычисления арифметических выражений.
 * * Класс реализует алгоритм рекурсивного спуска (LL(1) парсер) для обработки строковых 
 * математических выражений. Поддерживает базовые арифметические операции, приоритеты 
 * операторов и вложенные круглые скобки.
 * * Грамматика парсера:
 * @code
 * expression = term { ('+' | '-') term }
 * term       = factor { ('*' | '/') factor }
 * factor     = number | '(' expression ')'
 * @endcode
 * * @tparam T Тип данных, используемый для вычислений. Должен удовлетворять концепту ArithmeticNumber.
 */
template <ArithmeticNumber T>
class Calculator {
public:
    /**
     * @brief Вычисляет арифметическое выражение, переданное в виде строки.
     * * Метод инициализирует строковый поток ввода и запускает рекурсивный разбор выражения.
     * В конце валидирует отсутствие лишнего «мусора» в строке.
     * * @param expr Строка, содержащая математическое выражение (например, "2.5 + (3 * 4)").
     * @return T Результат вычисления выражения в типе данных шаблона.
     * @note Метод не является потокобезопасным из-за использования внутреннего указателя на поток.
     * @throws std::runtime_error Если в выражении есть синтаксические ошибки, 
     * лишние символы в конце или произошло деление на ноль.
     */
    T evaluate(const std::string &expr);

private:
    /**
     * @brief Указатель на текущий поток ввода, из которого посимвольно читается выражение.
     */
    std::istringstream *pos;

    /**
     * @brief Парсит и вычисляет выражение верхнего уровня (сложение и вычитание).
     * @return T Текущий результат вычисления на уровне выражений.
     */
    T parseExpression();
    
    /**
     * @brief Парсит и вычисляет терм (умножение и деление).
     * @return T Текущий результат вычисления на уровне термов.
     * @throws std::runtime_error При попытке деления на ноль.
     */
    T parseTerm();
    

    T parsePower();


    /**
     * @brief Парсит и вычисляет фактор (число или подвыражение внутри скобок).
     * @return T Значение считанного числа или результат вычисления внутри скобок.
     * @throws std::runtime_error Если отсутствует закрывающая круглая скобка ')'.
     */
    T parseFactor();
    
    /**
     * @brief Считывает числовое значение из входного потока.
     * @return T Считанное число в формате шаблона.
     * @throws std::runtime_error Если поток не смог распознать число в данном месте.
     */
    T parseNumber();
    
    /**
     * @brief Возвращает следующий непробельный символ из потока, не извлекая его.
     * @return char Следующий доступный символ.
     */
    char peek();
    
    /**
     * @brief Извлекает и возвращает следующий непробельный символ из потока.
     * @return char Извлеченный символ.
     */
    char get();
    
    /**
     * @brief Пропускает все пробельные символы во входном потоке.
     */
    void skipSpaces();

    constexpr T ipow(T base, T exp) {
        if (exp < 0) throw std::runtime_error("Отрицательная степень для целого числа");
        T result = 1;
        while (exp > 0) {
            if (exp & 1) result *= base;
            base *= base;
            exp >>= 1;
        }
        return result;
    }
};

