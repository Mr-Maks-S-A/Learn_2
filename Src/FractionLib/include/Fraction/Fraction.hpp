/**
* @file Fraction.hpp
* @brief Класс для работы с обыкновенными дробями.
* * Данный файл содержит объявление класса Fraction, который реализует
* полноценный тип данных для представления и выполнения математических
* операций над обыкновенными дробями с целым числителем и знаменателем.
*/
#pragma once

#include <iostream>
#include <string>



/**
 * @class Fraction
 * @brief Класс, представляющий обыкновенную дробь.
 * * Класс автоматически следит за тем, чтобы дробь всегда находилась в 
 * несократимом виде, а знак минуса (при его наличии) переносился в числитель.
 */
class Fraction {
private:
    int numerator;   /**< Числитель дроби */
    int denominator; /**< Знаменатель дроби (всегда строго больше 0 после инициализации) */

    /**
     * @brief Вычисляет Наибольший Общий Делитель (НОД) двух целых чисел.
     * @details Использует классический итеративный алгоритм Евклида.
     * @param numerator Первое число.
     * @param denominator Второе число.
     * @return Наибольший общий делитель чисел numerator и denominator.
     */
    int gcd(int numerator, int denominator) const;

    /**
     * @brief Вспомогательный метод для приведения дроби к каноническому виду.
     * @details Сокращает дробь на НОД числителя и знаменателя, контролирует
     * знаки (минус может быть только в числителе).
     * @throw std::invalid_argument Если знаменатель равен нулю.
     */
    void reduce();

public:
    /**
     * @brief Конструктор дроби.
     * @details Создает дробь вида num/denom. Если параметры не переданы,
     * создает нулевую дробь (0/1). Автоматически вызывает метод reduce().
     * @param num Числитель дроби (по умолчанию 0).
     * @param denom Знаменатель дроби (по умолчанию 1).
     * @throw std::invalid_argument Передается из reduce(), если denom == 0.
     */
    Fraction(int num = 0, int denom = 1);

    /**
     * @brief Возвращает строковое представление дроби для тестирования и отладки.
     * @return Строка в формате "числитель/знаменатель" (например, "3/4" или "-1/2").
     */
    std::string dump() const;

    // =========================================================================
    // 1. Арифметика (Бинарные операторы)
    // =========================================================================

    /**
     * @brief Оператор сложения двух дробей.
     * @param other Вторая дробь.
     * @return Новая дробь, являющаяся результатом сложения.
     */
    Fraction operator+(const Fraction& other) const;

    /**
     * @brief Оператор вычитания двух дробей.
     * @param other Вычитаемая дробь.
     * @return Новая дробь, являющаяся результатом вычитания.
     */
    Fraction operator-(const Fraction& other) const;

    /**
     * @brief Оператор умножения двух дробей.
     * @param other Вторая дробь.
     * @return Новая дробь, являющаяся результатом умножения.
     */
    Fraction operator*(const Fraction& other) const;

    /**
     * @brief Оператор деления двух дробей.
     * @param other Дробь-делитель.
     * @return Новая дробь, являющаяся результатом деления.
     * @throw std::invalid_argument Если числитель дроби other равен нулю (деление на 0).
     */
    Fraction operator/(const Fraction& other) const;

    // =========================================================================
    // 2. Операторы сравнения
    // =========================================================================

    /**
     * @brief Проверяет две дроби на равенство.
     * @param other Дробь для сравнения.
     * @return true, если дроби равны, иначе false.
     */
    bool operator==(const Fraction& other) const;

    /**
     * @brief Проверяет две дроби на неравенство.
     * @param other Дробь для сравнения.
     * @return true, если дроби не равны, иначе false.
     */
    bool operator!=(const Fraction& other) const;

    /**
     * @brief Проверяет, меньше ли текущая дробь, чем другая.
     * @param other Дробь для сравнения.
     * @return true, если текущая дробь меньше other, иначе false.
     */
    bool operator< (const Fraction& other) const;

    /**
     * @brief Проверяет, больше ли текущая дробь, чем другая.
     * @param other Дробь для сравнения.
     * @return true, если текущая дробь больше other, иначе false.
     */
    bool operator> (const Fraction& other) const;

    /**
     * @brief Проверяет, меньше ли или равна текущая дробь другой.
     * @param other Дробь для сравнения.
     * @return true, если текущая дробь меньше или равна other, иначе false.
     */
    bool operator<=(const Fraction& other) const;

    /**
     * @brief Проверяет, больше ли или равна текущая дробь другой.
     * @param other Дробь для сравнения.
     * @return true, если текущая дробь больше или равна other, иначе false.
     */
    bool operator>=(const Fraction& other) const;

    // =========================================================================
    // 3. Унарные операторы
    // =========================================================================

    /**
     * @brief Унарный минус. Меняет знак числителя дроби на противоположный.
     * @return Копия исходной дроби с противоположным знаком.
     */
    Fraction operator-() const;

    /**
     * @brief Унарный плюс. Возвращает копию текущей дроби без изменений.
     * @return Копия текущей дроби.
     */
    Fraction operator+() const;
    
    // =========================================================================
    // 4. Инкремент / Декремент
    // =========================================================================

    /**
     * @brief Префиксный инкремент (++f). Увеличивает дробь на 1.
     * @return Ссылка на измененный текущий объект.
     */
    Fraction& operator++();

    /**
     * @brief Постфиксный инкремент (f++). Увеличивает дробь на 1.
     * @param[in] Неиспользуемый параметр-маркер для компилятора.
     * @return Копия объекта до изменения.
     */
    Fraction operator++(int);

    /**
     * @brief Префиксный декремент (--f). Уменьшает дробь на 1.
     * @return Ссылка на измененный текущий объект.
     */
    Fraction& operator--();

    /**
     * @brief Постфиксный декремент (f--). Уменьшает дробь на 1.
     * @param[in] Неиспользуемый параметр-маркер для компилятора.
     * @return Копия объекта до изменения.
     */
    Fraction operator--(int);
};